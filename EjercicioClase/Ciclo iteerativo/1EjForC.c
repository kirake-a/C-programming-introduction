//Ejercicio de conocimiento del ciclo for, sintaxis.
#include <stdio.h>

int main(){

    //Declaracion del inicio de la variable, declaracion del final de la variable, pasos que toma para aumentar y cerrar el ciclo.
    //En el caso del ciclo for, podremos inicializar la variable dentro del mismo ciclo, pero lo mas recomendable es hacerlo fuera.
    for (int i = 0; i <= 2; i++)
    {
        printf("Hello world\n");
    }

    return 0;
}